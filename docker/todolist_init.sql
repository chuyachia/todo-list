CREATE DATABASE IF NOT EXISTS TODOLIST;

GRANT ALL PRIVILEGES ON *.* to 'user'@'%';
FLUSH PRIVILEGES;

USE TODOLIST;

CREATE TABLE IF NOT EXISTS todos (
    id INTEGER AUTO_INCREMENT,
    title VARCHAR(255) NOT NULL,
    status TEXT NOT NULL,
    priority TEXT,
    description TEXT,
    PRIMARY KEY (id)
)  ENGINE=INNODB;